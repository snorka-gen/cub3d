#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t i;

	if (!dst && !src)
		return (0);
	i = ft_strlen(src) + 1;
	if (i < dstsize)
		ft_memcpy(dst, src, i);
	else if (dstsize != 0)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = '\0';
	}
	return (i - 1);
}
